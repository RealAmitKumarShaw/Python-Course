a = {1, 45, 22}
b = {45, 99, 101, 22, 10}

print(a.union(b))          # It will print the union of set a and b
print(b.union(a))          # It will print the union of set b and a
print(a.intersection(b))   # It will print the intersection of set a and b
print(b.intersection(a))   # It will print the intersection of set b and a
print(a.difference(b))     # It will print the difference of set a and b
print(b.difference(a))     # It will print the difference of set b and a