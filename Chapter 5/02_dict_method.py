marks = {
    "Amit": 98,
    "Subham": 68,
    "Rohan": 78
}

print(marks)            # It will print all key-value pairs
print(marks.items())    # It will print all key-value pairs in the form of Tuples
print(marks.keys())     # It will print all the keys
print(marks.values())   # It will print all the values
print(len(marks))      # It will print the number of key-value pairs in the dictionary